import argparse
import json
import sys
from pathlib import Path
from typing import Any, Dict, List

from lxml import etree

if __package__ is None or __package__ == "":
    sys.path.append(str(Path(__file__).resolve().parents[2]))


PAGE_NS = {"p": "http://schema.primaresearch.org/PAGE/gts/pagecontent/2019-07-15"}


def validate_page_xml(path: Path) -> Dict[str, Any]:
    errors: List[str] = []
    tree = etree.parse(str(path))
    text_regions = tree.findall(".//p:TextRegion", PAGE_NS)
    text_lines = tree.findall(".//p:TextLine", PAGE_NS)

    for region in text_regions:
        region_id = region.get("id", "")
        if region.find("p:Coords", PAGE_NS) is None:
            errors.append(f"Region {region_id} has no Coords")

    for line in text_lines:
        line_id = line.get("id", "")
        if line.find("p:Coords", PAGE_NS) is None:
            errors.append(f"TextLine {line_id} has no Coords")
        if line.find("p:Baseline", PAGE_NS) is None:
            errors.append(f"TextLine {line_id} has no Baseline")
        unicode_node = line.find("p:TextEquiv/p:Unicode", PAGE_NS)
        if unicode_node is None:
            errors.append(f"TextLine {line_id} has no TextEquiv/Unicode")
        elif unicode_node.text is None:
            errors.append(f"TextLine {line_id} has empty Unicode")

        parent = line.getparent()
        if parent is None or etree.QName(parent).localname != "TextRegion":
            errors.append(f"TextLine {line_id} is not nested in a TextRegion")

    return {
        "file": str(path),
        "valid": not errors,
        "num_regions": len(text_regions),
        "num_lines": len(text_lines),
        "num_unicode": len(tree.findall(".//p:TextEquiv/p:Unicode", PAGE_NS)),
        "errors": errors,
    }


def validate_directory(input_dir: str = "outputs/judicial_demo", output_dir: str = "outputs/page_xml_validation") -> Dict[str, Any]:
    output = Path(output_dir)
    output.mkdir(parents=True, exist_ok=True)
    root = Path(input_dir)
    xml_files = sorted(root.glob("page_*_canvas_*/page.xml"))
    if not xml_files:
        xml_files = sorted(root.glob("*.xml"))
    pages = [validate_page_xml(path) for path in xml_files]
    report = {
        "input_dir": input_dir,
        "num_pages": len(pages),
        "valid_pages": sum(1 for page in pages if page["valid"]),
        "total_lines": sum(page["num_lines"] for page in pages),
        "total_unicode": sum(page["num_unicode"] for page in pages),
        "pages": pages,
    }
    with (output / "page_xml_validation_report.json").open("w", encoding="utf-8") as handle:
        json.dump(report, handle, indent=2, ensure_ascii=False)
    with (output / "page_xml_validation_report.md").open("w", encoding="utf-8") as handle:
        handle.write("# PAGE XML validation\n\n")
        handle.write(f"- Pages: {report['num_pages']}\n")
        handle.write(f"- Valid pages: {report['valid_pages']}\n")
        handle.write(f"- Total lines: {report['total_lines']}\n")
        handle.write(f"- Total Unicode nodes: {report['total_unicode']}\n\n")
        for page in pages:
            page_path = Path(page["file"])
            heading = page_path.parent.name if page_path.name == "page.xml" else page_path.stem
            handle.write(f"## {heading}\n\n")
            handle.write(f"- Valid: {page['valid']}\n")
            handle.write(f"- Regions: {page['num_regions']}\n")
            handle.write(f"- Lines: {page['num_lines']}\n")
            handle.write(f"- Unicode: {page['num_unicode']}\n")
            if page["errors"]:
                handle.write("- Errors:\n")
                for error in page["errors"]:
                    handle.write(f"  - {error}\n")
            handle.write("\n")
    print(json.dumps(report, indent=2, ensure_ascii=False))
    return report


def main() -> None:
    parser = argparse.ArgumentParser(description="Validate PAGE XML files generated by the judicial demo.")
    parser.add_argument("--input-dir", default="outputs/judicial_demo")
    parser.add_argument("--output-dir", default="outputs/page_xml_validation")
    args = parser.parse_args()
    validate_directory(args.input_dir, args.output_dir)


if __name__ == "__main__":
    main()
